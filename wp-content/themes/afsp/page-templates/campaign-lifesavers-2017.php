  <h2 class="red center">2017 LIFESAVERS GALA HOST &amp; HONOREE BIOS</h2>
  <div class="about white">
    <p><strong>Chris Gethard (Host)</strong> is an actor, comedian, and writer as well as the star and creator of “The Chris Gethard Show,” 
        which catapulted from New York Public Access to network cable and was described by Rolling Stone magazine as “the wildest talk show on TV.” 
        Chris is the creator and host of the podcast “Beautiful/Anonymous,” in which he takes anonymous phone calls that often lead to unexpectedly 
        moving confessions and breathtaking moments of shared humanity...while still somehow managing to be funny. He is the author of the memoir 
        “A Bad Idea I'm About to Do: True Tales of Seriously Poor Judgment and Stunningly Awkward Adventure,” which received rave reviews from 
        celebrities including Amy Poehler, Seth Meyers, Patton Oswalt, and others. As an actor he has appeared in television and movies including 
        “The Office,” “Louie,” “Broad City,” “Inside Amy Schumer,” “The Heat,” “Bored to Death,” and many more. His off-Broadway show “Career Suicide,” 
        in which he spoke unflinchingly about his personal experience with mental health and suicidal impulses using a rare blend of honesty and humor, 
        was produced by Judd Apatow, and will debut as an HBO special in May 2017. Chris has become an advocate and public voice for mental health and 
        suicide prevention.</p>
    <p><strong>Instagram</strong>: Every day, millions of people come to Instagram to share their stories and find strength and comfort in community. 
        But there are times when people on Instagram want to offer support to someone who may be in distress but don’t know how best to reach out. 
        Last year Instagram introduced new tools that allow anyone to anonymously report when someone they care about might be in crisis. 
        Teams review these reports 24/7 and in more than 40 languages and work quickly to connect someone in need with needed resources. 
        Accepting the award on behalf of Instagram is Cliff Hopkins, global head of marketing.</p>
    <p><strong>Congressman Tim Murphy</strong> is currently serving his eighth term in the U.S. House of Representatives. The only practicing 
        psychologist in elected federal government, Dr. Murphy relies on four decades of experience to advocate for meaningful reforms in the U.S. 
        healthcare system. And he has become a true champion for mental health. Serving as co-chair of the Mental Health Caucus and a founding 
        member of the GOP Doctors Caucus, he focuses on policies to make healthcare more affordable and accessible for all families. As chairman of 
        the House Energy & Commerce's Subcommittee on Oversight & Investigations, Murphy led a multi-year investigation into our nation’s broken 
        mental health system. The outcome was his landmark crisis mental health reform legislation, the Helping Families in Mental Health Crisis 
        Act (H.R. 2646) which was signed into law in December 2016 as part of the 21st Century Cures Act. The law fosters evidence-based care, 
        addresses the shortage of psychiatric hospital beds, empowers caregivers, and helps patients get treatment well before their illness spirals 
        into crisis. AFSP is pleased to recognize Congressman Murphy for being a voice for patients and families with mental illness and for 
        helping to prevent suicide.</p>
    <p><strong>Allison Schmitt</strong> is an eight-time Olympic medal swimmer and three-time Olympian, making her one of the most decorated swimmers 
        in the history of the sport. A Canton, Michigan native and 11-time NCAA champion, Allison graduated from the University of Georgia with a 
        degree in psychology and minor in child & family development. She is passionate about helping those who struggle with mental health issues 
        and has been vocal about her personal experience with depression prompted by her cousin tragically taking her own life. Allison continues to 
        spread the message of “it’s okay to not be okay” with the hope of changing stigmas associated with mental illness and helping as many people 
        as she can.</p>
    <p><strong>Joan R. Asarnow, Ph.D.</strong>, is Professor of Psychiatry and Biobehavioral Sciences at the UCLA David Geffen School of Medicine, 
        where she directs the Youth Stress and Mood Program, a depression and suicide prevention program which provides clinical care for youth 
        depression and suicidal ideation and behavior. The program focuses on cognitive-behavioral treatments by providing clinical care, conducting 
        research, and engaging in education and training. She is a past president of the Society for Clinical Child and Adolescent Psychology, 
        Division 53 of the American Psychological Association. A clinical psychologist, Dr. Asarnow has dedicated her career to developing, testing, 
        and implementing evidence-based treatments for adolescents at risk for suicide in emergency department and primary care settings. She has 
        received grants from the National Institute of Mental Health, Centers for Disease Control, Agency for Healthcare Research and Quality, 
        American Foundation for Suicide Prevention, and MacArthur Foundation. Most recently she has received a “Zero Suicide” grant from the 
        National Institute of Mental Health. She is also the principal investigator of the “Center for Trauma-Informed Suicide, Self-Harm & Substance 
        Abuse Prevention & Treatment” sponsored by the Substance Abuse and Mental Health Services Administration.</p>
